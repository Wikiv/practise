#ifndef __LOG_ORDER_INFO_H__
#define __LOG_ORDER_INFO_H__

#include <iostream>
#include <string>

using namespace std;
#include "SLog.h"

namespace UBACLOGGER {
	/// Class LogOrderInfo for logging order details.
	class LogOrderInfo
	{
		public:
			/// Create LogOrderInfo with default properties `property'.
			LogOrderInfo();
			~LogOrderInfo();

			/// Header Order ID.
			const static string LogOrderInfo_ORDER_ID;
			/// Header Account ID.
			const static string LogOrderInfo_ACCOUNT_ID;
			/// Header Order Action - New (ADD) or Modify (Edit).
			const static string LogOrderInfo_ADD_OR_EDIT;
			/// Header Symbol.
			const static string LogOrderInfo_SYMBOL;
			/// Header Asset Type (Equity, Forex, Future, Option etc.
			const static string LogOrderInfo_ASSET_TYPE;
			/// Header Quantity.
			const static string LogOrderInfo_QUANTITY;
			/// Header Order Type (Limit/Market...).
			const static string LogOrderInfo_ORDER_TYPE;
			/// Header Parent Order ID (in case of EDIT).
			const static string LogOrderInfo_PARENT_ORDER;
			/// Header Device Type (BB, iPhone etc.).
			const static string LogOrderInfo_DEVICE_TYPE;
			/// Header Broker Name.
			const static string LogOrderInfo_BROKER_NAME;
			/// Header Account Type (Demo/Real).
			const static string LogOrderInfo_ACCOUNT_TYPE;
			/// Header Time stamp (when order request received).
			const static string LogOrderInfo_TIME_STAMP;
			/// Connection Type (demo/real).
			const static string LogOrderInfo_CONNECTION_TYPE;
			/// Timezone.
			const static string LogOrderInfo_TIME_ZONE;
			/// OrderSide
			const static string LogOrderInfo_ACTION;
	};
};

using namespace UBACLOGGER;

#endif /* __LOG_ORDER_INFO_H__ */
